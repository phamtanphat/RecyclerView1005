package khoapham.ptp.phamtanphat.recyclerview1005;

public class Monan {
    private int id;
    private int hinhanh;

    public Monan(int id, int hinhanh) {
        this.id = id;
        this.hinhanh = hinhanh;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(int hinhanh) {
        this.hinhanh = hinhanh;
    }
}
